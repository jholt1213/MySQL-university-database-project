JOIN GRADE_CONVERSION ON GRADE.NUMERIC_GRADE BETWEEN GRADE_CONVERSION.MIN_GRADE AND GRADE_CONVERSION.MAX_GRADE
WHERE GRADE.GRADE_TYPE_CODE = 'FF'
ORDER BY STUDENT.STUDENT_ID ASC, SECTION.SECTION_ID ASC;

 SELECT
	CONCAT(s.SECTION_ID, ' ',c.DESCRIPTION) AS 'Section & Course Information',
    CONCAT(st.STUDENT_ID, ' ',st.FIRST_NAME, ' ', st.LAST_NAME) AS 'Student',
    g.NUMERIC_GRADE AS 'Calculated Grade'
FROM GRADE g
JOIN STUDENT st ON g.STUDENT_ID = st.STUDENT_ID
JOIN SECTION s ON g.SECTION_ID = s.SECTION_ID
JOIN COURSE c ON s.COURSE_NO = c.COURSE_NO
JOIN (SELECT
	SECTION_ID,
    AVG(NUMERIC_GRADE) AS AVG_GRADE
    FROM GRADE
    GROUP BY
		SECTION_ID) AS AVG_TABLE
        ON g.SECTION_ID = AVG_TABLE.SECTION_ID
	WHERE
		g.grade_type_code = 'FF' AND g.SECTION_ID BETWEEN 80 AND 90
        AND g.NUMERIC_GRADE > AVG_TABLE.AVG_GRADE
	ORDER BY
		g.SECTION_ID,
		st.STUDENT_ID;
        
SELECT
  C.COURSE_NO AS "Course No",
  C.DESCRIPTION AS "Description",
  CONCAT('$', FORMAT(C.COST, 2)) AS "Cost",
  P.COURSE_NO AS "Prerequisite",
  P.DESCRIPTION AS "Prereq.Description"
FROM COURSE AS C
LEFT JOIN COURSE AS P
  ON C.PREREQUISITE = P.COURSE_NO
ORDER BY
  C.COURSE_NO;

	
