--- This is created by AA. Create one record in the GRADE table per student, per section with a final code FF


create table grade_sum_1 as 
    select g.student_id, g.section_id, g.grade_type_code, gw.percent_of_final_grade,  
avg(g.numeric_grade)*percent_of_final_grade/100 AS SUM_GRADE 
from grade g, grade_type_weight gw 
where g.section_id=gw.section_id and g.grade_type_code=gw.grade_type_code 
group by g.student_id, g.section_id, g.grade_type_code, gw.percent_of_final_grade;

insert into grade (student_id, section_id, grade_type_code, grade_code_occurrence,numeric_grade, comments)     
select student_id, section_id, 'FF', '1', sum(sum_grade), null 
from grade_sum_1  
group by student_id, section_id; 

insert into grade_type_weight (section_id, grade_type_code, int_per_section, percent_of_final_grade, drop_lowest)
select distinct section_id, grade_type_code, 1, 100, 'N' 
from grade where grade_type_code = 'FF';

